def decode_image(vae_, latents_):
    dlat = 1 / vae_.config.scaling_factor * latents_
    dimg = vae_.decode(dlat)['sample']
    dimg = (dimg / 2 + 0.5).clamp(0, 1)
    dimg = dimg.cpu().permute(0, 2, 3, 1).detach().numpy()
    dimg = (dimg * 255).astype(np.uint8)

    return dimg

